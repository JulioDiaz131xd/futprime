class Player {
  final String id;
  final String name;
  final String imageUrl;
  final String description;
  final String team;
  final String position;
  final int goals;

  Player({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.description,
    required this.team,
    required this.position,
    required this.goals,
  });
}
