class Stadium {
  final String id;
  final String name;
  final String imageUrl;
  final String description;
  final String location;
  final int capacity;
  final int yearBuilt;

  Stadium({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.description,
    required this.location,
    required this.capacity,
    required this.yearBuilt,
  });
}
